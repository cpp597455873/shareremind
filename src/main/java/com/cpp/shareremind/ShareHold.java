package com.cpp.shareremind;


public class ShareHold {
    public String name;
    public String code;
    public Double cost;
    public Double share;
    public String date;

    public ShareHold(String name, String code, Double cost, Double share, String date) {
        this.name = name;
        this.code = code;
        this.cost = cost;
        this.share = share;
        this.date = date;
    }
}
